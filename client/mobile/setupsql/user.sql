CREATE TABLE IF NOT EXISTS `user` (
  `id` BIGINT(20) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '',
  `email` VARCHAR(255) NOT NULL COMMENT '',
  `password` TEXT NULL COMMENT '',
  `firstname` VARCHAR(255) NULL COMMENT '',
  `lastname` VARCHAR(255) NULL COMMENT '',
  `birthday` DATETIME NULL COMMENT '',
  `phonenumber` VARCHAR(50) NULL COMMENT '',
  `image` LONGBLOB NULL COMMENT '',
  `address` TEXT NULL COMMENT '',
  `postalcode` VARCHAR(255) NULL COMMENT '',
  `occupation` VARCHAR(255) NULL COMMENT '',
  `sex` INT NULL COMMENT '',
  `uuid` VARCHAR(255) NULL COMMENT '',
  `status` INT NULL COMMENT '',
  `created` DATETIME NULL DEFAULT CURRENT_TIMESTAMP COMMENT '',
  `updated` DATETIME NULL DEFAULT CURRENT_TIMESTAMP COMMENT '',
  PRIMARY KEY (`id`)  COMMENT '',
  UNIQUE INDEX `email_UNIQUE` (`email` ASC)  COMMENT '')
ENGINE = InnoDB;
